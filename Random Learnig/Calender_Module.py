import calendar
# calendar of month
print(calendar.month(2000, 4))
# calendar of year
print(calendar.calendar(2000))
# weekday method
print(calendar.weekday(2017, 3, 9))
# is leap year or not
print(calendar.isleap(2000))
print(calendar.isleap(2017))
# leapyear between years
print(calendar.leapdays(2015, 2022))